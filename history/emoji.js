function getNameToCode() {
	if (nameToCode === null) {
		nameToCode = {};
		for (let key in categories) {
			const emojis = categories[key];
			for (let i = 0, len = emojis.length; i < len; i += 1) {
				const info = emojis[i];
				nameToCode[info[0]] = info[1];
			}
		}
	}

	return nameToCode;
}

function textFromCode(code) {
	const codePoints = code.split('-').map(v => parseInt(v, 16)),
		text = String.fromCodePoint.apply(String, codePoints);

	return text;
}

